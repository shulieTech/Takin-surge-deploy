package io.shulie.surge.data;

import io.shulie.surge.data.common.factory.GenericFactorySpec;

public class HDFSSupportSpec implements GenericFactorySpec<HDFSSupport> {
    private String dir;

    private String fileName;

    private String dateFormat;

    public String getDir() {
        return dir;
    }

    public void setDir(String dir) {
        this.dir = dir;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getDateFormat() {
        return dateFormat;
    }

    public void setDateFormat(String dateFormat) {
        this.dateFormat = dateFormat;
    }

    @Override
    public String factoryName() {
        return "RollingHDFS";
    }

    @Override
    public Class<HDFSSupport> productClass() {
        return HDFSSupport.class;
    }

}
